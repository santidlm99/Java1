package ejercicios.repaso;

public class Calculadora {


public double suma(double numero,double numero1) {
		
	return numero+numero1;
}
public double resta(double numero,double numero1) {
		
	return numero-numero1;	
}
public double multiplicacion(double numero,double numero1) {
		
		return numero*numero1;
}

public double division(double numero,double numero1) {
		
			return numero/numero1;

		}
	}
